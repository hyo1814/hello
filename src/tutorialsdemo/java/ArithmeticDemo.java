package tutorialsdemo.java;

public class ArithmeticDemo {

	public static void main(String[] args) {
        int a = 10;
        int b = 3;
          
        float c = 10.0F;
        float d = 3.0F;
          
        System.out.println(a/b); //정수를 정수로 나누었기때문에 3.3333 소숫점 뒤에는 버림 
        System.out.println(c/d);
        System.out.println(a/d);
	}

}
